const { createClient } = require('@supabase/supabase-js')
require('dotenv').config()

const supabaseUrl = process.env.SUPABASE_URL || process.env.NEXT_PUBLIC_SUPABASE_URL
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY

console.log('üóëÔ∏è Testando Opera√ß√µes DELETE com Supabase...')

if (!supabaseUrl || !supabaseServiceKey) {
  console.error('‚ùå Credenciais do Supabase n√£o configuradas!')
  process.exit(1)
}

const supabase = createClient(supabaseUrl, supabaseServiceKey)

// Classe para opera√ß√µes DELETE
class DeleteOperations {
  constructor() {
    this.supabase = supabase
  }

  // DELETE - Remover registro por ID
  async deleteById(tableName, id) {
    try {
      console.log(`üóëÔ∏è Removendo registro ${id} de ${tableName}...`)
      
      // Verificar se o registro existe
      const { data: existingRecord, error: fetchError } = await this.supabase
        .from(tableName)
        .select('id')
        .eq('id', id)
        .single()

      if (fetchError || !existingRecord) {
        console.log(`‚ùå Registro ${id} n√£o encontrado em ${tableName}`)
        return { success: false, error: 'Registro n√£o encontrado' }
      }

      // Realizar a exclus√£o
      const { error } = await this.supabase
        .from(tableName)
        .delete()
        .eq('id', id)

      if (error) throw error

      console.log(`‚úÖ Registro ${id} removido com sucesso de ${tableName}`)
      return { success: true }
    } catch (error) {
      console.error(`‚ùå Erro ao remover registro:`, error.message)
      return { success: false, error: error.message }
    }
  }

  // DELETE - Remover m√∫ltiplos registros com filtros
  async deleteWithFilters(tableName, filters) {
    try {
      console.log(`üóëÔ∏è Removendo registros de ${tableName} com filtros:`, filters)
      
      let query = this.supabase.from(tableName).delete()
      
      // Aplicar filtros
      Object.entries(filters).forEach(([key, value]) => {
        if (value !== undefined && value !== null) {
          query = query.eq(key, value)
        }
      })
      
      const { error, count } = await query
      
      if (error) throw error

      console.log(`‚úÖ ${count} registros removidos de ${tableName}`)
      return { success: true, count }
    } catch (error) {
      console.error(`‚ùå Erro ao remover com filtros:`, error.message)
      return { success: false, error: error.message }
    }
  }

  // DELETE - Soft delete (marcar como exclu√≠do)
  async softDelete(tableName, id) {
    try {
      console.log(`üè∑Ô∏è Marcando registro ${id} como exclu√≠do em ${tableName}...`)
      
      // Verificar se o registro existe
      const { data: existingRecord, error: fetchError } = await this.supabase
        .from(tableName)
        .select('id')
        .eq('id', id)
        .single()

      if (fetchError || !existingRecord) {
        console.log(`‚ùå Registro ${id} n√£o encontrado em ${tableName}`)
        return { success: false, error: 'Registro n√£o encontrado' }
      }

      // Marcar como exclu√≠do (soft delete) usando apenas o campo ativo
      const { data, error } = await this.supabase
        .from(tableName)
        .update({ 
          ativo: false
        })
        .eq('id', id)
        .select()

      if (error) throw error

      console.log(`‚úÖ Registro ${id} marcado como exclu√≠do:`, data[0])
      return { success: true, data: data[0] }
    } catch (error) {
      console.error(`‚ùå Erro no soft delete:`, error.message)
      return { success: false, error: error.message }
    }
  }

  // DELETE - Restaurar registro (desfazer soft delete)
  async restore(tableName, id) {
    try {
      console.log(`üîÑ Restaurando registro ${id} em ${tableName}...`)
      
      // Verificar se o registro existe
      const { data: existingRecord, error: fetchError } = await this.supabase
        .from(tableName)
        .select('id, ativo')
        .eq('id', id)
        .single()

      if (fetchError || !existingRecord) {
        console.log(`‚ùå Registro ${id} n√£o encontrado em ${tableName}`)
        return { success: false, error: 'Registro n√£o encontrado' }
      }

      // Restaurar registro
      const { data, error } = await this.supabase
        .from(tableName)
        .update({ 
          ativo: true
        })
        .eq('id', id)
        .select()

      if (error) throw error

      console.log(`‚úÖ Registro ${id} restaurado:`, data[0])
      return { success: true, data: data[0] }
    } catch (error) {
      console.error(`‚ùå Erro ao restaurar:`, error.message)
      return { success: false, error: error.message }
    }
  }

  // DELETE - Verificar depend√™ncias antes de excluir
  async checkDependencies(tableName, id) {
    try {
      console.log(`üîç Verificando depend√™ncias do registro ${id} em ${tableName}...`)
      
      let dependencies = []
      
      // Verificar depend√™ncias baseadas na tabela
      switch (tableName) {
        case 'usuarios':
          // Verificar se o usu√°rio tem itens, coment√°rios, etc.
          const { data: userItems } = await this.supabase
            .from('itens')
            .select('id')
            .eq('usuario_id', id)
          
          if (userItems && userItems.length > 0) {
            dependencies.push(`itens (${userItems.length} registros)`)
          }
          break
          
        case 'categorias':
          // Verificar se a categoria tem itens
          const { data: categoryItems } = await this.supabase
            .from('itens')
            .select('id')
            .eq('categoria_id', id)
          
          if (categoryItems && categoryItems.length > 0) {
            dependencies.push(`itens (${categoryItems.length} registros)`)
          }
          break
          
        case 'itens':
          // Verificar se o item tem coment√°rios
          const { data: itemComments } = await this.supabase
            .from('comentarios')
            .select('id')
            .eq('item_id', id)
          
          if (itemComments && itemComments.length > 0) {
            dependencies.push(`comentarios (${itemComments.length} registros)`)
          }
          break
      }
      
      if (dependencies.length > 0) {
        console.log(`‚ö†Ô∏è Depend√™ncias encontradas: ${dependencies.join(', ')}`)
        return { success: false, dependencies }
      } else {
        console.log(`‚úÖ Nenhuma depend√™ncia encontrada`)
        return { success: true, dependencies: [] }
      }
    } catch (error) {
      console.error(`‚ùå Erro ao verificar depend√™ncias:`, error.message)
      return { success: false, error: error.message }
    }
  }

  // DELETE - Exclus√£o em cascata (remover depend√™ncias primeiro)
  async deleteCascade(tableName, id) {
    try {
      console.log(`üåä Realizando exclus√£o em cascata do registro ${id} em ${tableName}...`)
      
      // Verificar depend√™ncias
      const dependencyCheck = await this.checkDependencies(tableName, id)
      
      if (!dependencyCheck.success) {
        console.log(`‚ùå N√£o √© poss√≠vel excluir: existem depend√™ncias`)
        return dependencyCheck
      }
      
      // Se n√£o h√° depend√™ncias, realizar exclus√£o normal
      return await this.deleteById(tableName, id)
    } catch (error) {
      console.error(`‚ùå Erro na exclus√£o em cascata:`, error.message)
      return { success: false, error: error.message }
    }
  }
}

